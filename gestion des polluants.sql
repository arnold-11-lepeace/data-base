CREATE TABLE type_station_tst (
  tst_id NUMBER(2),
  tst_nom VARCHAR2(30),
  CONSTRAINT tst_pk PRIMARY KEY(tst_id)
);

CREATE TABLE station_stt (
  stt_id NUMBER(2),
  stt_nom VARCHAR2(40),
  stt_ville VARCHAR2(32),
  stt_adr VARCHAR2(100),
  stt_date_inst DATE,
  stt_tst_id NUMBER(2),
  CONSTRAINT stt_pk PRIMARY KEY(stt_id),
  CONSTRAINT stt_tst_id_fk FOREIGN KEY(stt_tst_id) REFERENCES type_station_tst(tst_id)
  );
  
CREATE TABLE capteur_cpr (
  cpr_id NUMBER(4),
  cpr_date_mes DATE,
  cpr_stt_id NUMBER(2),
  CONSTRAINT cpr_pk PRIMARY KEY(cpr_id),
  CONSTRAINT cpr_stt_id_fk FOREIGN KEY(cpr_stt_id) REFERENCES station_stt(stt_id)
);

CREATE TABLE polluant_plt (
  plt_id NUMBER(3),
  plt_nom VARCHAR2(40),
  plt_seuil_info NUMBER(5,2),
  plt_seuil_com VARCHAR2(50),
  CONSTRAINT plt_pk PRIMARY KEY(plt_id)
);

CREATE TABLE capte_cpt (
  cpt_plt_id NUMBER(3),
  cpt_cpr_id NUMBER(4),
  CONSTRAINT cpt_pk PRIMARY KEY(cpt_plt_id, cpt_cpr_id),
  CONSTRAINT cpt_plt_id_fk FOREIGN KEY(cpt_plt_id) REFERENCES polluant_plt(plt_id),
  CONSTRAINT cpt_cpr_id_fk FOREIGN KEY(cpt_cpr_id) REFERENCES capteur_cpr(cpr_id)
);


CREATE TABLE releve_rlv (
  rlv_id NUMBER(10),
  rlv_date DATE,
  rlv_heure NUMBER(2) ,
  rlv_mesure NUMBER(5,2),
  rlv_cpr_id NUMBER(4),
  rlv_plt_id NUMBER(3),
  CONSTRAINT rlv_pk PRIMARY KEY(rlv_id),
  CONSTRAINT rlv_plt_id_fk FOREIGN KEY(rlv_plt_id) REFERENCES polluant_plt(plt_id),
  CONSTRAINT rlv_cpr_id_fk FOREIGN KEY(rlv_cpr_id) REFERENCES capteur_cpr(cpr_id),
  CONSTRAINT rlv_heure_ck CHECK (rlv_heure BETWEEN 1 AND 24)
